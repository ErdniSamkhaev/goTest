// Создание канала для передачи числа
func square(n int, ch chan int) {
	ch <- n * n
}

func main() {
	ch := make(chan int)
	// Запускаем три горутины для чисел 2, 3, 4
	go square(2, ch)
	go square(3, ch)
	go square(4, ch)
	// Забираем три результата из канала
	for i := 0; i < 3; i++ {
		fmt.Println(<-ch)
	}
}
